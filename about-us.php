				<h2 class="headline">What Is The PBA?</h2>
				<p>The Florida Police Benevolent Association, Inc. (PBA) was established in 1972. A professional association of law enforcement officers, the PBA is a strong, steady advocate for law enforcement officers and their families. PBA specializes in protecting law enforcement officers through legal, legislative and political action.</p>

				<p>Times have changed since 1972, but Florida PBA has not forgotten its goals and we will continue to furnish the best, most effective representation possible.</p>

				<p>United we can set and accomplish even greater goals!</p>
				
				<h2>About the Gulf Coast Chapter</h2>				

				<p>The Gulf Coast PBA is a professional law enforcement association based in Fort Myers, Florida, and is a strong chapter of the Florida PBA.</p>

				<p>We are a member-driven organization, representing the needs of law enforcement workers on a multitude of tasks including collective bargaining negotiations, investigatory and disciplinary proceedings, legislative and public policy goals, legal defense and grievances. We represent both municipal agency officers and county sheriff's deputies.</p>

				<p>As a new member of the Gulf Coast PBA, you are covered from A to Z, with no additional charges for what is often considered an "enhanced benefit" with other associations, such as legal representation.</p>

				<p>Through our extensive involvement with Florida PBA, our members have the peace of mind that the best resources in the community, state and country are working for their best interests on the job every day.</p>

				<p>Please <a href="contact-us.php" class="bodyLink">Contact Us</a> for more information or to become a member of the Gulf Coast PBA.</p>				
	
